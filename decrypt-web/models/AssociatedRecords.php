    // Check if virtual fields is used in SQL
    protected function useVirtualFields()
    {
        $where = $this->UseSessionForListSql ? $this->getSessionWhere() : $this->CurrentFilter;
        $orderBy = $this->UseSessionForListSql ? $this->getSessionOrderByList() : "";
        if ($where != "") {
            $where = " " . str_replace(["(", ")"], ["", ""], $where) . " ";
        }
        if ($orderBy != "") {
            $orderBy = " " . str_replace(["(", ")"], ["", ""], $orderBy) . " ";
        }
        if (
            $this->linked_record_id->AdvancedSearch->SearchValue != "" ||
            $this->linked_record_id->AdvancedSearch->SearchValue2 != "" ||
            ContainsString($where, " " . $this->linked_record_id->VirtualExpression . " ")
        ) {
            return true;
        }
        if (ContainsString($orderBy, " " . $this->linked_record_id->VirtualExpression . " ")) {
            return true;
        }
        return false;
    }

