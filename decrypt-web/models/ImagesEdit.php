        $row['name'] = $this->name->DefaultValue;
        $row['path'] = $this->path->DefaultValue;
        $row['record_id'] = $this->record_id->DefaultValue;
        $row['page_number'] = $this->page_number->DefaultValue;
        $row['last_modified'] = $this->last_modified->DefaultValue;
        $row['processed'] = $this->processed->DefaultValue;
        return $row;
    }

    // Load old record
    protected function loadOldRecord()
    {
        // Load old record
        if ($this->OldKey != "") {
            $this->setKey($this->OldKey);
            $this->CurrentFilter = $this->getRecordFilter();
            $sql = $this->getCurrentSql();
            $conn = $this->getConnection();
            $rs = LoadRecordset($sql, $conn);
            if ($rs && ($row = $rs->fields)) {
                $this->loadRowValues($row); // Load row values
                return $row;
            }
        }
        $this->loadRowValues(); // Load default row values
        return null;
    }

